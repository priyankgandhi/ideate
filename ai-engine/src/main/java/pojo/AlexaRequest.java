package pojo;

public class AlexaRequest {

    String requestId;
    
    String Intent;

	public String getRequestId() {
		return requestId;
	}

	public void setRequestId(String requestId) {
		this.requestId = requestId;
	}

	public String getIntent() {
		return Intent;
	}

	public void setIntent(String intent) {
		Intent = intent;
	}


}
